public class SelectiveFlowOfControl {

	public static void main(String[] args) {
		
		// creating a new integer variable called season
		int season = 2;
		
		// this if statement checks what the variable season is equal to
		// if 
		if (season == 1) {
			System.out.println("Spring");
		} else if (season == 2) {
			System.out.println("Summer");
		} else if (season == 3) {
			System.out.println("Autumn");
		} else if (season == 4) {
			System.out.println("Winter");
		} else {
			System.out.println("Invalid Season");
		}

	}

}
