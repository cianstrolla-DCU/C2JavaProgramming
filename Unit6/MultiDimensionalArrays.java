/*
 * This class demonstrates the creation and referencing of a multi dimensional array 
 */


public class MultiDimensionalArrays {

	public static void main(String[] args) {
		
		int[][] myInts = { {1, 3, 5, 7}, {2, 4, 6} };
		
		int x = myInts[1][2];
		
		System.out.println(x); // Outputs 6

	}

}
