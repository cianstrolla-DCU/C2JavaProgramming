public class SequentialFlowOfControl {

	public static void main(String[] args) {
		
		// new integer x is set to 2 + 3 = 5
		int x = 2 + 3;
		
		// new integer y is equal to x * 2 = 10
		int y = x * 2;
		
		System.out.println(y);
		
		// sequential flow of control means the statements are
		// executed in order
		

	}

}
